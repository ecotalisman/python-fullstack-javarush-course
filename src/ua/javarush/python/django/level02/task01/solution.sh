# Creating a Virtual Environment
#
# 1. Create a new virtual environment using the built-in `venv` module. Name it "prod_env".
#
# 2. Activate the previously created "prod_env" virtual environment.
#
# 3. Deactivate the previously activated virtual environment.
#
# Requirements:
#
# 1. A new virtual environment named "prod_env" must be created using the built-in `venv` module.
# 2. The created "prod_env" virtual environment must be activated using the appropriate command for the operating system.
# 3. After activation, the "prod_env" virtual environment must be deactivated.
#
# 🇺🇦 Ukrainian version:
#
# Створення віртуального оточення
#
# 1. Створи нове віртуальне оточення за допомогою вбудованого модуля `venv`. Назви його "prod_env".
#
# 2. Активуй раніше створене віртуальне оточення "prod_env".
#
# 3. Деактивуй раніше активоване віртуальне оточення.
#
# Вимоги:
#
# 1. Необхідно створити нове віртуальне оточення з іменем "prod_env" з використанням вбудованого модуля `venv`.
# 2. Слід активувати створене віртуальне оточення "prod_env" з використанням відповідної команди для операційної системи.
# 3. Після активації потрібно виконати деактивацію віртуального оточення "prod_env".

# Create a virtual environment named "prod_env"
python -m venv prod_env

# Activate the created virtual environment
prod_env\Scripts\activate

# Deactivate the virtual environment
deactivate