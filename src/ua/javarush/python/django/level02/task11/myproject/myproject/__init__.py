# Development Server Launch Instructions

# To start the development server, run the following commands in the terminal from the project root directory, where manage.py is located:

# 1. Apply migrations, which will create the required tables in the database:

# python manage.py migrate

# 2. Start the development server:

# python manage.py runserver

# After that, the project will be available by default at http://127.0.0.1:8000/
