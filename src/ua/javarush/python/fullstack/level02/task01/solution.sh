# Running the First Container
# Run a simple container based on the hello-world image to make sure that Docker is installed
# and working correctly on your machine. The command must output a greeting message from Docker.
#
# Requirements:
# • The container must be created and started based on the official hello-world image from Docker Hub.
# • The container start command must run without errors, confirming that Docker is installed correctly.
# • After starting the container, a greeting message from Docker must appear in the terminal, confirming a successful launch.
#
# ## 🇺🇦 Ukrainian version:
#
# Запуск першого контейнера
# Запустіть простий контейнер на основі образу hello-world, щоб переконатися, що Docker встановлено
# і він працює правильно на вашій машині. Команда має вивести привітальне повідомлення від Docker.
#
# Вимоги:
# • Контейнер має бути створений і запущений на основі офіційного образу hello-world з Docker Hub.
# • Команда запуску контейнера повинна виконуватись без помилок, що підтверджує коректну установку Docker.
# • Після запуску контейнера в терміналі повинно з'являтися привітальне повідомлення від Docker, що підтверджує успішний запуск.

# Running a container based on the hello-world image
docker run hello-world