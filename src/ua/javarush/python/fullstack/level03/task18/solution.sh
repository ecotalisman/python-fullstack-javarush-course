# Removing a Specific Image
# Remove the image named nginx with the latest tag from your system using a Docker command.
# The image must no longer exist on the local machine.
#
# Requirements:
# • A Docker command must be used to remove the image from the local machine.
# • The command must include the image name 'nginx' and the tag 'latest' for precise removal.
# • After running the command, the image named 'nginx' with the tag 'latest' must no longer exist on the local system.
#
# ## 🇺🇦 Ukrainian version:
#
# Видалення конкретного образу
# Видаліть образ з ім'ям nginx і тегом latest з вашої системи, використовуючи команду Docker.
# Образ більше не повинен існувати на локальній машині.
#
# Вимоги:
# • Команда Docker повинна бути використана для видалення образу з локальної машини.
# • Команда повинна містити вказівку імені образу 'nginx' та тега 'latest' для точного видалення.
# • Після виконання команди, образ з ім'ям 'nginx' та тегом 'latest' повинен більше не існувати на локальній системі.

# Removing the Docker image named 'nginx' with the 'latest' tag
docker rmi nginx:latest